package Model;

public class Profile {
	private String firstName;
	private String lastName;
	private String jobTitle;
	private String nmlsId;
	private String directPhNo;
	private String mobileNo;
	private String address;
	
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getJobTitle() {
		return jobTitle;
	}
	public void setJobTitle(String jobTitle) {
		this.jobTitle = jobTitle;
	}
	public String getNmlsId() {
		return nmlsId;
	}
	public void setNmlsId(String string) {
		this.nmlsId = string;
	}
	public String getDirectPhNo() {
		return directPhNo;
	}
	public void setDirectPhNo(String directPhNo) {
		this.directPhNo = directPhNo;
	}
	public String getMobileNo() {
		return mobileNo;
	}
	public void setMobileNo(String mobileNo) {
		this.mobileNo = mobileNo;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	

}
